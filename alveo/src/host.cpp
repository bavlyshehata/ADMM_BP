std::vector<float> matrixVectorMultiply(const std::vector<float>& A, const std::vector<float>& x, int m, int n) {
    std::vector<float> result(m, 0);
    for (int i = 0; i < m; ++i) {
        for (int j = 0; j < n; ++j) {
            result[i] += A[i * n + j] * x[j];
        }
    }
    return result;
}

